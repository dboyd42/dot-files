# ----------------------------------------------------------------------------
# Quit running waybar instances
# ----------------------------------------------------------------------------
killall waybar

# ----------------------------------------------------------------------------
# Load the configuration
# ----------------------------------------------------------------------------
configFile="~/.config/waybar/waybar.config"
if [ -f $configFile ]; then
    waybar -c ~/.config/waybar/waybar.config & \
    -s ~/.config/waybar/style.css
else
    waybar &
fi

